public class Ejercicio7App {

	public static void main(String[] args) {
		//Declaramos una variable n y creamos un bucle While que se ira repitiendo mientras suma 1 cada vez que se ejecuta enseñandolo hasta llegar a 100 y entonces parar.
		int n=0;
		while(n<100) {
			n++;
			System.out.println(n);
		}

	}

}
